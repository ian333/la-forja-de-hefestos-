export interface MachineConfig {
  id: string;           // generated UUID
  fileName: string;     // original filename
  vendor: string;
  model: string;
  description: string;
  capabilities: MachineCapability[];

  // CNC sections (present when capabilities includes 'milling' or 'turning')
  controller?: MachineController;
  kinematics?: KinematicNode[];
  machining?: MachineMachining;
  tooling?: MachineTooling;
  multiaxis?: MachineMultiaxis;
  post?: MachinePost;
  connection?: MachineConnection;

  // Additive sections (present when capabilities includes 'additive')
  additiveTechnology?: string;
  extruders?: AdditiveExtruder[];
  limits?: AdditiveLimits;
  platform?: AdditivePlatform;

  // Fusion metadata
  fusion?: MachineFusion;

  // Interaction pairs count (for collision checking)
  interactionPairs?: number;

  // Computed summaries
  axisCount: number;
  maxSpindleSpeed: number;
  workEnvelope: { x: number; y: number; z: number };
}


let _machineIdCounter = 0;

function generateMachineId(): string {
  return `mch_${Date.now()}_${++_machineIdCounter}`;
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseController(raw: any): MachineController | undefined {
  if (!raw) return undefined;

  // Controllers can be under 'default' or 'synced_configuration'
  const configName = raw.default ? 'default' : raw.synced_configuration ? 'synced_configuration' : Object.keys(raw)[0];
  const config = raw[configName];
  if (!config) return undefined;

  const axes: MachineAxis[] = [];

  // Parts contain axes
  if (config.parts) {
    for (const [name, part] of Object.entries(config.parts)) {
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      const p = part as any;
      axes.push({
        name,
        coordinate: p.coordinate ?? 0,
        maxNormalSpeed: p.max_normal_speed ?? 0,
        maxRapidSpeed: p.max_rapid_speed ?? 0,
        tcp: p.tcp ?? false,
        preference: p.preference ?? 'no preference',
        wrapAroundMin: p.wrap_around_at_min,
        wrapAroundMax: p.wrap_around_at_max,
      });
    }
  }

  return {
    configName,
    axes,
    headPartId: config.head_info_part_id,
    tablePartId: config.table_part_id,
    tcpRapidMode: config.tcp_rapid_interpolation_mode,
    maxNormalSpeed: config.max_normal_speed,
  };
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseKinematics(raw: any): KinematicNode[] {
  if (!raw) return [];

  // Actual Fusion 360 structure: raw.default.parts = flat array of part nodes
  const kinDef = raw.default ?? raw;
  const rawParts: unknown[] = kinDef.parts;
  if (!Array.isArray(rawParts)) return [];

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  function parseNode(p: any): KinematicNode {
    const node: KinematicNode = {
      id: p.name ?? p.id ?? 'unknown',
      // 'driven' = motorized axis. is_rotational flag distinguishes rotary vs linear.
      type: p.control === 'driven'
        ? (p.is_rotational ? 'rotary' : 'linear')
        : (p.type ?? 'static'),
      children: [],
    };

    if (p.direction) node.direction = p.direction as [number, number, number];
    if (p.min !== undefined) node.rangeMin = p.min;
    if (p.max !== undefined) node.rangeMax = p.max;
    if (p.home !== undefined) node.home = p.home;

    // Spindle / head detection
    if (p.spindle) {
      node.role = 'head';
      node.spindle = {
        maxSpeed: p.spindle.max_speed ?? 0,
        peakTorque: p.spindle.peak_torque,
        peakTorqueSpeed: p.spindle.peak_torque_speed,
        power: p.spindle.power,
        coolants: p.tool_station?.coolants
          ? Object.keys(p.tool_station.coolants)
          : undefined,
      };
    } else if (p.table_station !== undefined) {
      node.role = 'table';
    }

    // Recurse into nested parts array
    if (Array.isArray(p.parts)) {
      for (const child of p.parts) {
        node.children.push(parseNode(child));
      }
    }

    return node;
  }

  return rawParts.map(parseNode);
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseMachining(raw: any): MachineMachining | undefined {
  if (!raw) return undefined;
  const cfg = raw.default || raw;
  return {
    feedrateRatio: cfg.feedrate_ratio ?? 1,
    toolChangeTime: cfg.tool_change_time ?? 15,
  };
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseTooling(raw: any): MachineTooling | undefined {
  if (!raw) return undefined;
  const cfg = raw.default || raw;
  return {
    hasToolChanger: cfg.has_tool_changer ?? false,
    numberOfTools: cfg.number_of_tools ?? 0,
    supportsToolPreload: cfg.supports_tool_preload ?? false,
  };
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseMultiaxis(raw: any): MachineMultiaxis | undefined {
  if (!raw) return undefined;
  const cfg = raw.default || raw;
  return {
    singularityAdjust: cfg.singularity?.adjust ?? false,
    singularityMethod: cfg.singularity?.method,
    singularityTolerance: cfg.singularity?.tolerance,
    feedMethod: cfg.feedrate?.method,
    maxFeedrate: cfg.feedrate?.max_feedrate,
    virtualToolTip: cfg.virtual_tool_tip ?? false,
    linearizationTolerance: cfg.linearization_tolerance,
    retractAndReconfigure: cfg.retract_and_reconfigure?.enabled,
  };
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parsePost(raw: any): MachinePost | undefined {
  if (!raw) return undefined;
  const cfg = raw.default || raw;
  const result: MachinePost = {
    path: cfg.path ?? '',
  };
  if (cfg.properties && Object.keys(cfg.properties).length > 0) {
    result.properties = cfg.properties;
  }
  return result;
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseFusion(raw: any): MachineFusion | undefined {
  if (!raw) return undefined;
  return {
    guid: raw.guid,
    image: raw.image,
    modelUrn: raw.model_urn,
    imageAutoGenerated: raw.imageAutoGenerated,
  };
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function parseConnection(rawConn: any, rawConnector: any): MachineConnection | undefined {
  if (!rawConn && !rawConnector) return undefined;
  const conn = rawConn?.default?.ethernet ?? {};
  const connector = rawConnector?.default ?? {};
  return {
    ip: connector.ip ?? conn.ip ?? '',
    port: conn.port ?? connector.port ?? 0,
    connectorPath: connector.path,
    connectorIdent: connector.ident,
  };
}

// Compute work envelope from kinematics
function computeWorkEnvelope(kinematics: KinematicNode[]): { x: number; y: number; z: number } {
  const envelope = { x: 0, y: 0, z: 0 };

  const walk = (nodes: KinematicNode[]) => {
    for (const node of nodes) {
      if (node.type === 'linear' && node.rangeMin !== undefined && node.rangeMax !== undefined) {
        const travel = Math.abs(node.rangeMax - node.rangeMin);
        const dir = node.direction;
        if (dir) {
          if (Math.abs(dir[0]) > 0.5) envelope.x = Math.max(envelope.x, travel);
          else if (Math.abs(dir[1]) > 0.5) envelope.y = Math.max(envelope.y, travel);
          else if (Math.abs(dir[2]) > 0.5) envelope.z = Math.max(envelope.z, travel);
        }
      }
      walk(node.children);
    }
  };

  walk(kinematics);
  return envelope;
}

// Find max spindle speed from kinematics tree
function findMaxSpindleSpeed(kinematics: KinematicNode[]): number {
  let max = 0;
  const walk = (nodes: KinematicNode[]) => {
    for (const node of nodes) {
      if (node.spindle && node.spindle.maxSpeed > max) {
        max = node.spindle.maxSpeed;
      }
      walk(node.children);
    }
  };
  walk(kinematics);
  return max;
}


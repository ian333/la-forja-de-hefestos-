/**
 * ⚒️ La Forja de Hefestos — Machine Configuration Parser
 * ========================================================
 * Parses Fusion 360 .mch files (JSON) into typed TypeScript structures.
 * Supports: CNC milling (3/4/5-axis), turning, additive (FFF/FDM).
 *
 * Based on analysis of 8 real Fusion 360 machine configs:
 *   - Bambu Lab P1P (additive FFF)
 *   - Brother SPEEDIO M300X3 (5-axis mill+turn)
 *   - DATRON neo (3-axis)
 *   - GROB G350 (5-axis)
 *   - HAAS EC-630 (4-axis horizontal)
 *   - HAAS VF-2 (3-axis)
 *   - HAAS VS-3 (3-axis)
 *   - Hurco BX40i (3-axis)
 */

// ═══════════════════════════════════════════════════════════════
// Core Types
// ═══════════════════════════════════════════════════════════════

export type MachineCapability = 'milling' | 'turning' | 'additive';
export type AxisType = 'linear' | 'rotary' | 'static';
export type FeedMethod = 'FPM' | 'inverse time';

export interface MachineAxis {
  name: string;
  coordinate: number;
  maxNormalSpeed: number;
  maxRapidSpeed: number;
  tcp: boolean;
  preference: string;
  wrapAroundMin?: number;
  wrapAroundMax?: number;
}

export interface MachineSpindle {
  maxSpeed: number;
  peakTorque?: number;
  peakTorqueSpeed?: number;
  power?: number;
  coolants?: string[];
}

export interface KinematicNode {
  id: string;
  type: AxisType;
  direction?: [number, number, number];
  rotationAxis?: [number, number, number];
  rangeMin?: number;
  rangeMax?: number;
  home?: number;
  offset?: [number, number, number];
  role?: 'head' | 'table' | 'spindle' | 'tool' | string;
  spindle?: MachineSpindle;
  children: KinematicNode[];
}

export interface MachineController {
  configName: string; // 'default' or 'synced_configuration'
  axes: MachineAxis[];
  headPartId?: string;
  tablePartId?: string;
  tcpRapidMode?: string;
  maxNormalSpeed?: number;
}

export interface MachineMachining {
  feedrateRatio: number;
  toolChangeTime: number;
}

export interface MachineTooling {
  hasToolChanger: boolean;
  numberOfTools: number;
  supportsToolPreload: boolean;
}

export interface MachineMultiaxis {
  singularityAdjust: boolean;
  singularityMethod?: string;
  singularityTolerance?: number;
  feedMethod?: FeedMethod;
  maxFeedrate?: number;
  virtualToolTip: boolean;
  linearizationTolerance?: number;
  retractAndReconfigure?: boolean;
}

export interface MachinePost {
  path: string;
  properties?: Record<string, unknown>;
}

export interface MachineFusion {
  guid?: string;
  image?: string; // base64 PNG
  modelUrn?: string;
  imageAutoGenerated?: boolean;
}

export interface MachineConnection {
  ip: string;
  port: number;
  connectorPath?: string;
  connectorIdent?: string;
}

// ── Additive-specific ──

export interface AdditiveExtruder {
  index: number;
  diameter: number;
  filamentDiameter: number;
  maxTemperature: number;
  maxVolumePerSec: number;
}

export interface AdditiveLimits {
  accelXYMax: number;
  accelZMax: number;
  speedXYMax: number;
  speedZMax: number;
  bedTempMax: number;
}

export interface AdditivePlatform {
  size: [number, number, number]; // [X, Y, Z] in mm
  origin: [number, number, number];
}


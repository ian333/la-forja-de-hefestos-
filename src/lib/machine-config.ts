/**
 * Parse a Fusion 360 .mch file (JSON string) into a MachineConfig.
 */
export function parseMachineConfig(json: string, fileName: string = 'unknown.mch'): MachineConfig {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const raw: any = JSON.parse(json);

  const general = raw.general?.default ?? raw.general ?? {};
  const capabilities: MachineCapability[] = (general.capabilities ?? []) as MachineCapability[];

  const controller = parseController(raw.controller);
  const kinematics = parseKinematics(raw.kinematics);
  const machining = parseMachining(raw.machining);
  const tooling = parseTooling(raw.tooling);
  const multiaxis = parseMultiaxis(raw.multiaxis);
  const post = parsePost(raw.post);
  const fusion = parseFusion(raw.fusion);
  const connection = parseConnection(raw.connection, raw.connector);

  // Additive
  let additiveTechnology: string | undefined;
  let extruders: AdditiveExtruder[] | undefined;
  let limits: AdditiveLimits | undefined;
  let platform: AdditivePlatform | undefined;

  if (raw.additive) {
    const addDefault = raw.additive.default ?? raw.additive;
    additiveTechnology = addDefault.technology;
  }

  if (raw.additive_extruder) {
    extruders = [];
    for (const [key, val] of Object.entries(raw.additive_extruder)) {
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      const ext = val as any;
      extruders.push({
        index: parseInt(key) || 0,
        diameter: ext.diameter ?? 0.4,
        filamentDiameter: ext.filament_diameter ?? 1.75,
        maxTemperature: ext.max_temperature ?? 260,
        maxVolumePerSec: ext.max_volume_per_sec ?? 15,
      });
    }
  }

  if (raw.limits?.default) {
    const lim = raw.limits.default;
    limits = {
      accelXYMax: lim.accel_xy_max ?? 0,
      accelZMax: lim.accel_z_max ?? 0,
      speedXYMax: lim.speed_xy_max ?? 0,
      speedZMax: lim.speed_z_max ?? 0,
      bedTempMax: lim.bed_temp_max ?? 0,
    };
  }

  if (raw.platform?.default?.dimensions) {
    const dim = raw.platform.default.dimensions;
    platform = {
      size: dim.size ?? [200, 200, 200],
      origin: dim.origin ?? [0, 0, 0],
    };
  }

  // Compute summaries
  const axisCount = controller?.axes.length ?? 0;
  const maxSpindleSpeed = findMaxSpindleSpeed(kinematics);
  const workEnvelope = capabilities.includes('additive') && platform
    ? { x: platform.size[0], y: platform.size[1], z: platform.size[2] }
    : computeWorkEnvelope(kinematics);

  // Interaction pairs count
  let interactionPairs: number | undefined;
  if (raw.interactions?.default) {
    interactionPairs = Object.keys(raw.interactions.default).length;
  }

  return {
    id: generateMachineId(),
    fileName,
    vendor: general.vendor ?? 'Unknown',
    model: general.model ?? 'Unknown',
    description: general.description ?? '',
    capabilities,
    controller,
    kinematics,
    machining,
    tooling,
    multiaxis,
    post,
    connection,
    fusion,
    additiveTechnology,
    extruders,
    limits,
    platform,
    interactionPairs,
    axisCount,
    maxSpindleSpeed,
    workEnvelope,
  };
}

/**
 * Import a .mch file from a File object.
 */
export async function importMachineFile(file: File): Promise<MachineConfig> {
  const text = await file.text();
  return parseMachineConfig(text, file.name);
}

/**
 * Get a short display string for a machine
 */
export function machineDisplayString(m: MachineConfig): string {
  const caps = m.capabilities.join('+');
  const axes = m.axisCount > 0 ? `${m.axisCount}-axis` : '';
  const envelope = m.workEnvelope.x > 0
    ? `${m.workEnvelope.x}×${m.workEnvelope.y}×${m.workEnvelope.z}mm`
    : '';

  return [m.vendor, m.model, `(${caps}${axes ? ' ' + axes : ''})`, envelope]
    .filter(Boolean)
    .join(' ');
}

/**
 * Get machine type icon for UI display
 */
export function machineIcon(m: MachineConfig): string {
  if (m.capabilities.includes('additive')) return '🖨';
  if (m.capabilities.includes('turning')) return '🔄';
  if (m.axisCount >= 5) return '⚙️';
  if (m.axisCount >= 4) return '🔧';
  return '🏭';
}

/**
 * Get envelope as formatted string
 */
export function formatEnvelope(m: MachineConfig): string {
  const { x, y, z } = m.workEnvelope;
  if (x === 0 && y === 0 && z === 0) return 'N/A';
  return `${x} × ${y} × ${z} mm`;
}

/**
 * Get all machine attributes as omnibar-searchable entries
 */
export function machineSearchEntries(machines: MachineConfig[]): {
  id: string;
  label: string;
  description: string;
  category: string;
  icon: string;
  machine: MachineConfig;
}[] {
  return machines.map(m => ({
    id: m.id,
    label: `${m.vendor} ${m.model}`,
    description: `${m.capabilities.join(', ')} · ${m.axisCount} ejes · ${formatEnvelope(m)} · ${m.maxSpindleSpeed > 0 ? m.maxSpindleSpeed + ' RPM' : ''}`,
    category: 'Máquinas',
    icon: machineIcon(m),
    machine: m,
  }));
}
